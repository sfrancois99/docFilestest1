    private void openJButtonActionPerformed(java.awt.event.ActionEvent evt) {                                            
        openFile();
    }                                           

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {                                    
        if (evt.getClickCount()==2){
            openFile();
        }
    }                                   

    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {                                              
        int index=this.jList1.getSelectedIndex();
        if (index<0){
            return;
        }
        if (listModel.getSize() > 0){
            listModel.removeElementAt(index);
            if (index<listModel.getSize()){
                this.jList1.setSelectedIndex(index);
            } else {
                this.jList1.setSelectedIndex(listModel.getSize()-1);
            }
        }
    }                                             
    private void openFile(){
        int index=this.jList1.getSelectedIndex();
        DocFile file = (DocFile)listModel.get(index);
        IHMDocFileDetails ihm = new IHMDocFileDetails(null, true, file);
        ihm.pack();
        ihm.setVisible(true);
    }

